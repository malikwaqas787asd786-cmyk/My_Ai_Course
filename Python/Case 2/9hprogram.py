# Indexing 
str="Salman"
ch=str[3]
print(ch)

fruits = ["apple", "banana", "mango"]
print(fruits[0])   # apple
print(fruits[2])  # mango
print(fruits[1])   # banana

numbers = (10, 20, 30)
print(numbers[1])  # 20
print(numbers[0])  # 10
print(numbers[2])  # 30

# Negative Indexing
str="Salman"
ch=str[-4]
print(ch)

fruits = ["apple", "banana", "mango"]
print(fruits[-1])   # mango
print(fruits[-3])  # apple
print(fruits[-2])   # banana
numbers = (10, 20, 30)

print(numbers[-2])  # 20
print(numbers[-3])  # 10
print(numbers[-1])  # 30
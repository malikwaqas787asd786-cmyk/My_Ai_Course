#String Functions
str="i am studing python from apna college"
print(str.endswith("college"))   # True
print(str.count("a"))            # 3
print(str.capitalize())          # I am studing python from apna college
print(str.find("python"))       # 13
print(str.replace("am","was"))  # i was studing python from apna college
print(str.upper())              # I AM STUDING PYTHON FROM APNA COLLEGE
print(str.lower())              # i am studing python from apna college
print(str.title())              # I Am Studing Python From Apna College
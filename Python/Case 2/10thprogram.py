#Slicing Positive Indexing 
text = "waqas"
print(text[0:4])   
print(text[2:5])   
print(text[:3])    
print(text[3:])    

text = "Python"
print(text[0:4])   # Pyth
print(text[2:5])   # thon
print(text[:3])    # Pyt
print(text[3:])    # hon

name  = "Sarwar"
print(name[1:4])   # arw
print(name[2:4])
print(name[:3])    # Sar
print(name[3:len(name)])    # war

#Slicing Negative Indexing
word = "waqas"
print(word[-5:-2])   
print(word[-4:])    
print(word[:-3])     
print(word[-3:]) 
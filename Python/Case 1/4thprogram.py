#Assignment Operations
num=20
num=num+10 #addition
print("num =",num)
num=num/2  #division
print("num =",num)

num=num%6  #modulus
print("num =",num)

num=num//3 #floor division  
print("num =",num)

num=num-5  #subtraction
print("num =",num)

num=num*2  #multiplication
print("num =",num)

num=num**2 #exponentiation
print("num =",num)